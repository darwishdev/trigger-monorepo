func TestErrorAuth_badKey(t *testing.T) {
	base := os.Getenv("CRM_BASE_URL")
	if base == "" {
		base = "http://localhost:8069"
	}
	c, err := NewClient(sales.CRMConfig{Provider: "odoo", BaseURL: base, APIKey: "definitely-not-a-real-key"})
	if err != nil {
		t.Fatalf("NewClient: %v", err)
	}

	_, err = c.LeadList(context.Background(), sales.LeadFilter{Limit: 1})
	if !errors.Is(err, sales.ErrAuth) {
		t.Fatalf("expected ErrAuth, got %v", err)
	}
}

