// newTestClient skips the test when no API key is available (e.g. CI), so the
// suite degrades gracefully outside a developer machine with Odoo running. It
// reads the same CRM_* keys the app itself uses (from config/dev.env).
func newTestClient(t *testing.T) sales.CRM {
	t.Helper()
	key := os.Getenv("CRM_API_KEY")
	if key == "" {
		t.Skip("CRM_API_KEY not set; skipping Odoo integration test")
	}
	base := os.Getenv("CRM_BASE_URL")
	if base == "" {
		base = "http://localhost:8069"
	}
	c, err := NewClient(sales.CRMConfig{Provider: "odoo", BaseURL: base, APIKey: key})
	if err != nil {
		t.Fatalf("NewClient: %v", err)
	}
	return c
}

func TestLeadList(t *testing.T) {
	c := newTestClient(t)
	ctx := context.Background()

	page, err := c.LeadList(ctx, sales.LeadFilter{Limit: 5})
	if err != nil {
		t.Fatalf("LeadList: %v", err)
	}
	if page.Count == nil || *page.Count == 0 {
		t.Fatal("expected leads in demo data, got count=0")
	}
	if len(page.Results) == 0 {
		t.Fatal("expected results, got none")
	}

	l := page.Results[0]
	if l.ID == "" || l.Name == "" {
		t.Fatalf("lead not adapted: %+v", l)
	}
	if l.Budget.Currency == "" {
		t.Errorf("budget currency not mapped: %+v", l.Budget)
	}
	if l.Tags == nil {
		t.Error("Tags should be a non-nil slice even when empty")
	}
	t.Logf("first lead: id=%s name=%q stage=%q budget=%v tags=%v", l.ID, l.Name, l.Stage, l.Budget, l.Tags)
}

func TestLeadFind(t *testing.T) {
	c := newTestClient(t)
	ctx := context.Background()

	page, err := c.LeadList(ctx, sales.LeadFilter{Limit: 1})
	if err != nil || len(page.Results) == 0 {
		t.Fatalf("need a lead to fetch: %v", err)
	}
	id := page.Results[0].ID

	detail, err := c.LeadFind(ctx, id)
	if err != nil {
		t.Fatalf("LeadFind(%s): %v", id, err)
	}
	if detail.ID != id {
		t.Errorf("detail id = %s, want %s", detail.ID, id)
	}
	if detail.OpenActivities == nil {
		t.Error("OpenActivities should be a non-nil slice")
	}
	t.Logf("lead %s: partner=%q team=%q deadline=%s open_activities=%d",
		detail.ID, detail.Partner, detail.SalesTeam, detail.Deadline, len(detail.OpenActivities))
}

