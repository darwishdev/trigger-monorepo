// TestActivityScheduleAndComplete exercises the full write path. Completing
// deletes the activity in Odoo and writes a chatter message, so the net effect
// on the demo DB is one new chatter line - acceptable for a local test Odoo.
func TestActivityScheduleAndComplete(t *testing.T) {
	c := newTestClient(t)
	ctx := context.Background()

	leads, err := c.LeadList(ctx, sales.LeadFilter{Limit: 1})
	if err != nil || len(leads.Results) == 0 {
		t.Fatalf("need a lead to attach an activity: %v", err)
	}
	leadID := leads.Results[0].ID

	created, err := c.ActivitySchedule(ctx, sales.ActivityDraft{
		LeadID:       leadID,
		Summary:      "Trigger integration test follow-up",
		Note:         "auto-generated by odooclient integration test",
		Deadline:     time.Now().AddDate(0, 0, 2),
		ActivityType: "mail.mail_activity_data_todo",
	})
	if err != nil {
		t.Fatalf("ActivitySchedule: %v", err)
	}
	if created.ID == "" {
		t.Fatalf("created activity has no id: %+v", created)
	}
	t.Logf("scheduled activity %s on lead %s", created.ID, leadID)

	result, err := c.ActivityComplete(ctx, created.ID, "done from integration test")
	if err != nil {
		t.Fatalf("ActivityComplete(%s): %v", created.ID, err)
	}
	if result.Completed.ID != created.ID {
		t.Errorf("completed id = %s, want %s", result.Completed.ID, created.ID)
	}
	if result.AuditRef == "" {
		t.Error("expected AuditRef (Odoo message_id) on completion")
	}
	t.Logf("completed activity %s; audit_ref=%s", result.Completed.ID, result.AuditRef)
}

